

public class Square {
	private double x;
	private double y;
	private int size;
	
	
	public Square(double x, double y, int size) {
		super();
		this.x = x;
		this.y = y;
		this.size = size;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	

}
